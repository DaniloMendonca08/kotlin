//Exercicio - Classificação Numérica

fun main () {
    val numero = 7 // definindo o número do valor a ser verificado

    if (numero > 0) {
        
        println("O número é positivo") //se for verdadeiro, imprime essa mensagem
    
    } else if (numero < 0) {
        
        println("O número é negativo")
    
    } else {
        
        println("O número é zero") //Se nenhuma das condições anteriores for verdadeira
    }
}